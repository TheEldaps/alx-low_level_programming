#include <stdio.h>
#include <stdlib.h>

/**
 * main - program entry point
 * @argc: stores the number arguments to the programme
 * @argv: array of strings passed to the programme
 * Return: exit of the programme
 *
 */

int main(int argc __attribute__((unused)), char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}
